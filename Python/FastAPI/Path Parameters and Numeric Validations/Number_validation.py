from typing import Annotated

from fastapi import FastAPI, Path

app = FastAPI()

# gt: >
# ge: >=
# le: <=
@app.get("/items/{item_id}")
async def read_items(
    item_id: Annotated[int, Path(title="The ID of the item to get", ge=1)], q: str
):
    results = {"item_id": item_id}
    if q:
        results.update({"q": q})
    return results


